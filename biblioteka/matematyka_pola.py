def pole_kwadratu(a):
    #Funkcja oblicza pole kwadratu.
    #input bok: float, długość boku kwadratu
    #return: float, pole kwadratu

    return a ** 2

def pole_prostokata(a, b):
    #Funkcja oblicza pole prostokąta.
    #input dlugosc: float, długość prostokąta
    #input szerokosc: float, szerokość prostokąta
    #return: float, pole prostokąta

    return a * b

def pole_trojkata(a, h):
    #Funkcja oblicza pole trójkąta.
    #input podstawa: float, długość podstawy trójkąta
    #input wysokosc: float, wysokość trójkąta
    #return: float, pole trójkąta"

    return 0.5 * a * h
